package com.company;

public class logical_operator_j13 {
    public static void main(String[] args) {
        //logical operators
        // AND Operator --> &&
        // OR Operator --> ||
        // Not Operator --> !
        boolean a = true;
        boolean c = false;
        System.out.println(a&&c);
        System.out.println(a||c);
        System.out.println(!a);
        System.out.println(!c);
        System.out.println(!a&&!c);
    }
}
