package com.company;

public class increment_and_decrement_operator_j8 {
    public static void main(String[] args) {
        // increment operator
        int i = 45;
        int b = i++;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        //decrement operator
        //float x = 65f;
        float y = 52;
        System.out.println(y--);
        System.out.println(y);
        System.out.println(--y);
        System.out.println(-y);
        System.out.println(y);

        char ch = 'a';
        System.out.println(++ch);
        System.out.println(ch++);

    }
}
